package src.main.java.utils;

public class Kalkulator {

    public static int Hitung (int angkaPertama, int angkaKedua){
        return angkaPertama + angkaKedua;
    };

    public static int Kurang (int angkaPertama, int angkaKedua){
        return angkaPertama - angkaKedua;
    };

    public static int Multiple (int angkaPertama, int angkaKedua){
        return angkaPertama * angkaKedua;
    };

    public static int Divide (int angkaPertama, int angkaKedua){
        return angkaPertama / angkaKedua;
    };
    

}